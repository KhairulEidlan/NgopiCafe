package com.example.ngopi.apps.model;

public class RvCat {
    private int image;
    private String text;

    public RvCat(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
